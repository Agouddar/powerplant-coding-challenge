using ENGIE.CC.RESTAPI.Models;
using ENGIE.CC.RESTAPI.Services;

var builder = WebApplication.CreateBuilder(args);

// Add services to the container.
// Learn more about configuring Swagger/OpenAPI at https://aka.ms/aspnetcore/swashbuckle
builder.Services.AddScoped<IProductionPlanGenerator, ProductionPlanGenerator>();
builder.Services.AddEndpointsApiExplorer();
builder.Services.AddSwaggerGen();


var app = builder.Build();

// Configure the HTTP request pipeline.
if (app.Environment.IsDevelopment())
{
    app.UseSwagger();
    app.UseSwaggerUI();
}


app.MapPost("/productionplan",  (ProductionPlanPayload payload , IProductionPlanGenerator generator) =>
{
    return  generator.Generate(payload);
})
.WithName("Calculate production plan");

app.Run();

